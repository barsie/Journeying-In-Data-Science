package Lessons;

public class ForLoopsIncrementals {
    public static void main(String[] args) {
        // For loops and while loops

        // For loop incremental iteration
        for (int i = 0; i< 5; i++){
            System.out.println("Hello World " + i);
        }

        System.out.println("-------------------------");

        // For loop incremental iterator initialized in 1
        for(int m = 1; m <= 5; m++)
            System.out.println("Hellow word index " + m);

        System.out.println("-------------------------");

        // For loop drecremental iteration
        for (int l = 5; l > 0; l--)
            System.out.println("Hello World " + l);

        System.out.println("---------------------------");
        // WHYLE LOOPS 
        
     
    }
}
